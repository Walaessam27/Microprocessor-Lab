CODE SEGMENT
ASSUME CS:CODE
ORG 2000H
START:
MOV AL, 80H
MOV DX, 0FF2BH , CWR
OUT DX, AL
MAIN:
MOV AL, 0FFH
MOV DX, 0FF29H ;PORT B
OUT DX, AL
MOV SI, OFFSET COLS
MOV CX,8
LPA:
MOV AL, [SI]
MOV DX, 0FF28H ;PORT A Indicates the 
beginning of the COLS ARRAY
OUT DX, AL
MOV AL, 0FFH
MOV DX, 0FF2AH ;PORT C ALL ROWS IS ON
OUT DX, AL
PUSH CX
MOV CX, 50000
DELAYS: NOP
LOOP DELAYS
POP CX
INC SI
LOOP LPA
MOV AL, 0FFH
MOV DX, 0FF28H ;PORT A
OUT DX, AL
MAIN1:
MOV SI, OFFSET COLS
MOV CX,8
LPB:
MOV AL, [SI]
MOV DX, 0FF29H ;PORT B
OUT DX, AL
MOV AL, 0FFH
MOV DX, 0FF2AH ;PORT C
OUT DX, AL
PUSH CX
MOV CX, 50000
DELAYS1: NOP
LOOP DELAYS1
POP CX
INC SI
LOOP LPB
JMP MAIN
COLS DB 0FEH, 0FDH, 0FBH, 0F7H, 
0EFH, 0DFH, 0BFH, 7FH
END START
CODE ENDS
