CODE SEGMENT
ASSUME CS: CODE 
ORG 2000H
START:
MOV AL, 0H ;SET
MOV DX, 0FF81H
OUT DX, AL
MOV AL, 32H ;CLOCK
OUT DX, AL
MOV AL, 0DFH ;CLEAR
OUT DX, AL
MOV CX, 0FFH ; DELAY
LP: NOP
LOOP LP
MAIN:
MOV AL, 92H ;SELECT THE FIRST 7-SEDMENT
MOV DX, 0FF81H
OUT DX, AL
MOV AL, 20H ;PRINT 6
MOV DX, 0FF80H 
OUT DX, AL
MOV AL, 0F3H ;PRINT -
MOV DX, 0FF80H 
OUT DX, AL
MOV AL, 0C0H ;PRINT P
MOV DX, 0FF80H 
OUT DX, AL
MOV AL, 020H ;PRINT G
MOV DX, 0FF80H 
OUT DX, AL
JMP MAIN
END START
CODE ENDS
