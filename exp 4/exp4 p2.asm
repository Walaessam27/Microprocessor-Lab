CODE SEGMENT
ASSUME CS: CODE
ORG 2000H
START:
MOV AL, 0H ;SET
MOV DX, 0FF81H
OUT DX, AL
MOV AL, 32H ;CLOCK
OUT DX, AL
MOV AL, 0DFH ;CLEAR
OUT DX, AL
MOV CX, 0FFH ; DELAY
LP: NOP
LOOP LP
START2:
MOV SI, OFFSET DARR
MOV CX,10
MOV AL, 80H ;SELECT THE FIRST 7-SEDMENT
MOV DX, 0FF81H
OUT DX, AL
MAIN:
MOV AL, [SI]
MOV DX, 0FF80H
OUT DX, AL
PUSH CX
MOV CX, 0FFFFH
LP1: NOP
LOOP LP1
POP CX
INC SI
LOOP MAIN
JMP START2
DARR DB 0CH, 9FH, 4AH, 0BH, 99H, 29H, 28H, 8FH, 08H, 89H
END START
CODE ENDS